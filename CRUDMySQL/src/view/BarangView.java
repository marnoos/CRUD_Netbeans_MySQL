    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
        String kode = txtKode.getText();
        String nama = txtNama.getText();
        double harga = Double.parseDouble(txtHarga.getText());

        model.Barang b = new model.Barang(kode, nama, harga);
        if (controller.ubahBarang(b)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Diperbarui!");
            refreshTabel();
            bersihkanForm();
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Harga harus berupa angka!");
    }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        try {
        String kode = txtKode.getText();
        String nama = txtNama.getText();
        double harga = Double.parseDouble(txtHarga.getText());

        model.Barang b = new model.Barang(kode, nama, harga);
        if (controller.tambahBarang(b)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan!");
            refreshTabel();
            bersihkanForm();
        }
    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Error Input", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        String kode = txtKode.getText();
    if (kode.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih data di tabel terlebih dahulu!");
        return;
    }
    
    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin hapus data ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        if (controller.hapusBarang(kode)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Data Berhasil Dihapus!");
            refreshTabel();
            bersihkanForm();
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnBersihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBersihActionPerformed
        // TODO add your handling code here:
        bersihkanForm();
    }//GEN-LAST:event_btnBersihActionPerformed

    private void tableBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBarangMouseClicked
        // TODO add your handling code here:
        int barisTerpilih = tableBarang.getSelectedRow();
    if (barisTerpilih != -1) {
        txtKode.setText(tableModel.getValueAt(barisTerpilih, 0).toString());
        txtNama.setText(tableModel.getValueAt(barisTerpilih, 1).toString());
        txtHarga.setText(tableModel.getValueAt(barisTerpilih, 2).toString());
    }
    }//GEN-LAST:event_tableBarangMouseClicked

