    private void refreshTabel() {
        tableModel.setRowCount(0);
        for (model.Barang b : controller.ambilSemuaBarang()) {
            tableModel.addRow(new Object[]{b.getKode(), b.getNama(), b.getHarga()});
        }
    }

    private void bersihkanForm() {
        txtKode.setText("");
        txtNama.setText("");
        txtHarga.setText("");
        txtKode.requestFocus();
    }

